'''Escreva um programa que receba três números e imprima sua soma:'''


numero_1 = int(input("Digitar o primeiro número a ser somado: "))
numero_2 = int(input("Digitar o segundo número a ser somado: "))
numero_3 = int(input("Digitar o terceiro número a ser somado: "))

soma_total = numero_1 + numero_2 + numero_3

print(f"A soma total é = {soma_total}")
